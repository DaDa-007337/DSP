void delay_ms(Uint16 ms) {
    Uint16 i,j; for(i=0;i<ms;i++) for(j=0;j<1000;j++);
}

// ✅ 模拟 SPI 模式3 发送（和你 STM32 代码 100% 匹配）
void LCD_Send(Uint8 dat)
{
    Uint8 i;
    for(i=0;i<8;i++)
    {
        SCK(0);
        if(dat & 0x80) SDI(1);
        else SDI(0);
        dat <<= 1;
        SCK(1);
    }
}

void LCD_WR_CMD(Uint8 cmd)
{
    CS(0); DC(0);
    LCD_Send(cmd);
    CS(1);
}

void LCD_WR_DATA(Uint8 dat)
{
    CS(0); DC(1);
    LCD_Send(dat);
    CS(1);
}

void LCD_SetWindow(Uint16 x1,Uint16 y1,Uint16 x2,Uint16 y2)
{
    LCD_WR_CMD(0x2A);
    LCD_WR_DATA(x1>>8); LCD_WR_DATA(x1&0xFF);
    LCD_WR_DATA(x2>>8); LCD_WR_DATA(x2&0xFF);
    LCD_WR_CMD(0x2B);
    LCD_WR_DATA(y1>>8); LCD_WR_DATA(y1&0xFF);
    LCD_WR_DATA(y2>>8); LCD_WR_DATA(y2&0xFF);
    LCD_WR_CMD(0x2C);
}

