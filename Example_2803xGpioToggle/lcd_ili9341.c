void LCD_DrawPoint(Uint16 x,Uint16 y,Uint16 color)
{
    if(x>=LCD_W || y>=LCD_H) return;
    LCD_SetWindow(x,y,x,y);
    LCD_WR_DATA(color>>8);
    LCD_WR_DATA(color&0xFF);
}

void LCD_Clear(Uint16 color)
{
    Uint16 i,j;
    LCD_SetWindow(0,0,LCD_W-1,LCD_H-1);
    for(i=0;i<LCD_W;i++)
        for(j=0;j<LCD_H;j++)
            { LCD_WR_DATA(color>>8); LCD_WR_DATA(color&0xFF); }
}

void LCD_ShowString(Uint16 x,Uint16 y,char *str,Uint16 color)
{
    Uint16 i,j,k;Uint8 ch;
    for(;*str;str++){
        ch=*str-'0';if(ch>9)continue;
        for(j=0;j<16;j++){
            Uint8 dt=ASCII[ch][j];
            for(k=0;k<8;k++)if(dt&(0x80>>k))LCD_DrawPoint(x+k,y+j,color);
        }
        x+=8;
    }
}

void LCD_ShowChinese(Uint16 x,Uint16 y,char *str,Uint16 color)
{
    Uint16 i,j,k;
    for(i=0;i<3;i++){
        for(j=0;j<16;j++){
            Uint8 t1=Chinese[i][j*2];
            Uint8 t2=Chinese[i][j*2+1];
            for(k=0;k<8;k++){
                if(t1&(0x80>>k))LCD_DrawPoint(x+k,y+j,color);
                if(t2&(0x80>>k))LCD_DrawPoint(x+k+8,y+j,color);
            }
        }
        x+=16;
    }
}